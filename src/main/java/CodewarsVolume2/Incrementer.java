package CodewarsVolume2;

import java.util.Arrays;

public class Incrementer {
  public static void main(String[] args) {
    // Пример входных данных
    int[] input = {4, 6, 9, 1, 3};

    // Вызываем метод incrementer и выводим результат
    int[] result = Kata.incrementer(input);
    System.out.println(Arrays.toString(result));
  }


  // План
  // Если массив пустой, то сразу возвращается пустой массив. Это можно сделать с помощью проверки длины массива.
  // Пройтись по каждому элементу массива с использованием цикла. Для этого можно использовать цикл for
  // Инкремент элемента:
  // Для каждого элемента массива: определить его индекс (позицию), начиная с 1 (а не с 0). Это можно сделать с помощью for
  // Прибавить позицию к текущему элементу.
  // Если сумма (элемент + его позиция) больше 9, взять только последнюю цифру результата. Это можно сделать
  // с помощью операции нахождения остатка от деления на 10
  // Сохранить результаты инкрементации в новом массиве, который будет возвращён как результат.
  // После завершения итерации вернуть новый массив.

  public static class Kata {

    public static int[] incrementer(int[] numbers) {
      // Если массив пустой, сразу возвращаем пустой массив
      if (numbers == null || numbers.length == 0) {
        return new int[0];
      }

      // Создаем новый массив для результата
      int[] result = new int[numbers.length];

      // Итерация по каждому элементу массива
      for (int i = 0; i < numbers.length; i++) {
        // Инкрементируем элемент с учетом его позиции (позиция = индекс + 1)
        result[i] = (numbers[i] + (i + 1)) % 10; // Берем остаток от деления на 10
      }

      return result;
    }
  }
}