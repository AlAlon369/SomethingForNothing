public class JavaForBegginersLesson3 {
    public static void main(String[] args) {
        int x = 5;
        String s = "Hello";  // начинается с заглавной, потому что String является классом
        // где s это переменная, а String это класс.
        String space = " ";
        String name = "Bob";
        // Для того чтобы объеденить эти строки нам нужно использовать оператор + при помощи метода sout.
        System.out.println(s+space+name);
        System.out.println("Hello"+ " "+ "John");
        System.out.println("My number is "+x); // где x это переменная хранящая в себе значение 5.




    }
}
